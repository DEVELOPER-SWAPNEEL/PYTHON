#THOSE TYPE OF FUNCTIONS WHICH DONOT CONTAIN ANY NAME IS KNOWN AS LAMBDA FUNCTION--->THESE ARE USED IN SIMPLER PROGRAMMINGS
'''NORMAL FUNCTIONS ARE HAVING CODE AS:-
def add(i,j):
    return i+j
print(add(5,2))
result is 7
'''
#-----------SYNTAX---------
#LAMBDA ARGUMENTS : EXPRESSION
result = lambda a: a+2
print(result(5))
#FOR MULTIPLE ARGUMENTS:-
op= lambda a,b: a+b 
print(op(5,10))
